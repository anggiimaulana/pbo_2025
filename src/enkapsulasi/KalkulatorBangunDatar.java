// Subclass kalkulator bangun datar
class KalkulatorBangunDatar {
    //    Metode persegi
    public double luasPersegi(double sisi) {
        return sisi * sisi;
    }
    
    //    metode lingkaran
    public double luasLingkaran(double radius) {
        return Math.PI * radius * radius;
    }
}